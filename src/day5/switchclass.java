package day5;

public class switchclass {	
	public static void main(String[] args) {	
		
		char a = '3';	
		
		switch (a) {
		case '1':
			System.out.println("one");
			break;
			
		case '2':
			System.out.println("this is my case 2");
			break;
			
		default:			
			System.out.println("Exit");
			break;
		}
	}
}
