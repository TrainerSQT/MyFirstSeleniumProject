package day3;

public class AddValue {
	
	public static void main(String[] args) {
		
		//Initialization
		int a = 10;
		int b = 20;
		
		//Declare
		int c;		
		c = a + b;		
		System.out.println(c);
		
		int c1 = a - b;		
		System.out.println(c1);
		
		int c2 = a * b;		
		System.out.println(c2);
		
		int c3 = a / b;		
		System.out.println(c3);		
	}

}
